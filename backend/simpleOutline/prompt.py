# 当用户上传或者输入的内容长度超过一定长度时，使用不同的prompt
# 设计的原因是，当用户上传一个PDF或者文章时，肯定是希望根据该文章生成大纲，进而使用该文章生成PPT，而不是网络搜索
# 如果用户输入的内容或者上传的内容很短，那么大概率需要通过网络搜索去搜索和扩充大纲，例如只输入1个topic，简单的话，那么
# 根据搜索去生成大纲更合理，所以当用户输入的长度大于USER_INPUT_NUMBER时，提示词变成只使用用户输入的内容生成大纲。
# 否则使用用户输入内容+网络搜索生成大纲
USER_INPUT_NUMBER = 1000

# 使用搜索生成大纲
OUTLINE_INSTRUCTION_WITH_SEARCH = """
你的唯一任务是输出一个 Markdown 大纲。严格遵守以下规则，不要输出任何其他内容：

【硬性输出约束】
1. 你的整个回复必须**且只能**是一个 Markdown 大纲，从字符 `# ` 开始。
2. 严禁在 `# 标题` 之前出现任何文字，包括但不限于：
   - "首先 / 接下来 / 我需要 / 我认为 / 可能的大纲如下 / 好的"
   - 任何解释、说明、过渡句、问候语、客套话
   - 任何 "```markdown" 之类的代码块标记（直接输出 Markdown 文本，不要用代码块包裹）
3. 严禁在最后一个 `- 要点` 之后追加任何结语、总结、致谢、备注。
4. 不要重复调用 DocumentSearch 工具超过 1 次；如果你已经搜到了足够的信息，必须**立即**直接输出大纲。

【大纲格式】
- 使用Markdown标题层级：# 标题 → ## 一级部分 → ### 二级小节 → 列表要点
- 一级部分数量：5个；每个一级部分下含3–4个二级小节
- 每个二级小节列出3–5个要点；要点使用短句，动词开头，不超过18字，不要句号
- 术语统一、风格一致，必要时加入可量化指标或示例
- 语言：{language}

【输出示例 - 注意是从 # 开始的纯 Markdown，没有前缀】
# 标题

## 一级部分
### 二级小节
- 要点1
- 要点2
- 要点3

### 二级小节
- 要点1
- 要点2
- 要点3
- 要点4
- 要点5
"""

# 不使用搜索生成大纲
